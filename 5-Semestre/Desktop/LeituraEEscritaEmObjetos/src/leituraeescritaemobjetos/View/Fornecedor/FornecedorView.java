    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        int row = jTable1.getSelectedRow();
        if(row  >=0){
            int id = Integer.parseInt( jTable1.getValueAt(row,0).toString());
            if (ConfirmaDialog.msgConfimarExclusao(jTable1.getValueAt(row,1).toString()))
            {
                fornecedorC.removerItem(id);
                getDm().removeRow(jTable1.getSelectedRow());
                fornecedorC.salvar();
            }
        }else{
            InformativoDialog.msgItemNaoSelecionado("excluir", true);
        }
        
    }//GEN-LAST:event_jToggleButton1ActionPerformed

    private void jToggleButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton2ActionPerformed
        int row = jTable1.getSelectedRow();
        if(row >=0){
            int id = Integer.parseInt( jTable1.getValueAt(jTable1.getSelectedRow(),0).toString());

            FornecedorCadView cliView = new FornecedorCadView(fornecedorC.retornarItem(id));
            cliView.setVisible(true);
        }else{
            InformativoDialog.msgItemNaoSelecionado("editar", true);
        }
        
    }//GEN-LAST:event_jToggleButton2ActionPerformed

