    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        
        ListItem comboboxCliente = (ListItem) jComboBoxCliente.getSelectedItem();
        
        ListItem comboboxProduto = (ListItem) jComboBoxProduto.getSelectedItem();
        System.out.println(comboboxProduto.getId());
        vendasController.getVendas().getCliente().setId(comboboxCliente.getId());
        vendasController.getVendas().getProduto().setId(comboboxProduto.getId());
        int qtd = Integer.parseInt(jTextFieldQtd.getText());
        vendasController.getVendas().setQuantidade(qtd);
        float preco = Float.parseFloat(jTextFieldPreco.getText());
        vendasController.getVendas().setPreco(preco);
        vendasController.getVendas().setTotal(qtd * preco);
        if(isNew){
            vendasController.inserir();
            JanVendastoLista.carregarLista();
        }else{
            vendasController.update();
            JanVendastoLista.carregarLista();
        }
        this.dispose();
    }//GEN-LAST:event_jButtonSalvarActionPerformed

