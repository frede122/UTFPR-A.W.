    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed

                int row = jTableJanCliente.getSelectedRow();
        if(row == -1){
            InformativoDialog.msgItemNaoSelecionado("Editar", true);
        }else{
            int id =  (jTableJanCliente.getValueAt(row, 0) != null) ? Integer.parseInt(jTableJanCliente.getValueAt(row, 0).toString()) : 0;
            String nome =  (jTableJanCliente.getValueAt(row, 1) != null) ? jTableJanCliente.getValueAt(row, 1).toString() : "";
            String cpf =  (jTableJanCliente.getValueAt(row, 2) != null) ? jTableJanCliente.getValueAt(row, 2).toString() : "";
            String endereco =  (jTableJanCliente.getValueAt(row, 3) != null) ? jTableJanCliente.getValueAt(row, 3).toString() : "";
            String email =  (jTableJanCliente.getValueAt(row, 4) != null) ? jTableJanCliente.getValueAt(row, 4).toString() : "";
            cliC.getCliente().setId(id);
            cliC.getCliente().setNome(nome);
            cliC.getCliente().setCpf(cpf);
            cliC.getCliente().setEndereco(endereco);
            cliC.getCliente().setEmail(email);
            JanClienteEditar ce = new JanClienteEditar(cliC.getCliente());
            ce.setLocationRelativeTo(this);
            ce.setVisible(true);
            
            
        }
        
        
    }//GEN-LAST:event_jButtonEditarActionPerformed

