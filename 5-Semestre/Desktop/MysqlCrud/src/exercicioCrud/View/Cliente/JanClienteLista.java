public static void carregarLista(){
        if(defaultModel == null){
            return;
        }
        defaultModel.setRowCount(0);
        defaultModel.setColumnCount(0);
        
        try {
            ResultSet rst = cliC.returnAll();
            int colNo = rst.getMetaData().getColumnCount();
            for (int i = 1; i <= rst.getMetaData().getColumnCount(); i++) {
                defaultModel.addColumn(rst.getMetaData().getColumnLabel(i));
            }
            
            while(rst.next()){
                Object[] obj = new Object[colNo];
                for(int i = 0; i < colNo; i++) {
                    obj[i] = rst.getObject(i+1);
                }
                defaultModel.addRow(obj);
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        
    }
