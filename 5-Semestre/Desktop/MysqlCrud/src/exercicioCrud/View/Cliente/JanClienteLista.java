    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        int row = jTableJanCliente.getSelectedRow();
        if(row == -1){
            InformativoDialog.msgItemNaoSelecionado("Excluir", true);
        }else{
            String nome =  (jTableJanCliente.getValueAt(row, 1) != null) ? jTableJanCliente.getValueAt(row, 1).toString() : "";
            if(ConfirmaDialog.msgConfimarExclusao(nome)){
                cliC.excluir(Integer.parseInt(jTableJanCliente.getValueAt(row, 0).toString()));
                JanClienteLista.carregarLista();
            }
            
            
        }
    }//GEN-LAST:event_jButtonExcluirActionPerformed

