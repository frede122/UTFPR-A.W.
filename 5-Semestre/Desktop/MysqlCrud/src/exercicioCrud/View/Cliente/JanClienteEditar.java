    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        
        clienteController.getCliente().setNome(jTextFieldNome.getText());
        clienteController.getCliente().setCpf(jTextFieldCPF.getText());
        clienteController.getCliente().setEndereco(jTextFieldEndereco.getText());
        clienteController.getCliente().setEmail(jTextFieldEmail.getText());
        
        if(isNew){
            clienteController.inserir();
            JanClienteLista.carregarLista();
        }else{
            clienteController.update();
            JanClienteLista.carregarLista();
        }
        this.dispose();
    }//GEN-LAST:event_jButtonSalvarActionPerformed

