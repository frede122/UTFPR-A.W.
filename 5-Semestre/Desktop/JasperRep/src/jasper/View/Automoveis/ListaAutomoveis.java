    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        int row = jTableListaModelos.getSelectedRow();
        if(row == -1){
            InformativoDialog.msgItemNaoSelecionado("Editar", true);
        }else{
            int id = Integer.parseInt(jTableListaModelos.getValueAt(row, 0).toString());
            String modelo = jTableListaModelos.getValueAt(row, 1).toString();
            int idfab = Integer.parseInt(jTableListaModelos.getValueAt(row, 2).toString());
            float gasolina = Float.parseFloat(jTableListaModelos.getValueAt(row, 3).toString());
            float alcool = Float.parseFloat(jTableListaModelos.getValueAt(row, 4).toString());
            float diesel = Float.parseFloat(jTableListaModelos.getValueAt(row, 5).toString());
            String fabricante = jTableListaModelos.getValueAt(row, 6).toString();
            
            ctr.getAutomovel().setId(id);
            ctr.getAutomovel().setModelo(modelo);
            ctr.getAutomovel().getFabricante().setId(idfab);
            ctr.getAutomovel().getFabricante().setNome(fabricante);
            ctr.getAutomovel().setGasolina_efici(gasolina);
            ctr.getAutomovel().setAlcool_efici(alcool);
            ctr.getAutomovel().setDiesel_efici(diesel);
            CadastrarAutomovel cadA = new CadastrarAutomovel(ctr);
            cadA.setLocationRelativeTo(this);
            cadA.setVisible(true);
        }
    }//GEN-LAST:event_jButtonEditarActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        int row = jTableListaModelos.getSelectedRow();
        if( row == -1 ){
            InformativoDialog.msgItemNaoSelecionado("Excluir", true);
        }else{
            if(ConfirmaDialog.msgConfimarExclusao(jTableListaModelos.getValueAt(row, 1).toString())){
                int id = Integer.parseInt(jTableListaModelos.getValueAt(row, 0).toString());
                ctr.getAutomovel().setId(id);
                if(ctr.delete() == 1){
                    getDefaultModel().removeRow(row);
                }
            }
        }
    }//GEN-LAST:event_jButtonExcluirActionPerformed

